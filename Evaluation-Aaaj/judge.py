def main() -> None:
    """Main entry point."""

    # -----------------------------------------------------------------------
    # Input files — edit these paths as needed
    # -----------------------------------------------------------------------

    # Ground truth file.
    # Required fields per record:
    #   variable_name, R1_id, application,
    #   annotated_short_description_R2, annotated_long_description_R2
    ground_truth_path: str = "Judge/input/GroundTruth.json"

    # Model result file.
    # Required fields per record:
    #   variable_name, R1_id, app_name,
    #   bob_output  — a dict whose keys (other than "variable_name") are
    #                 model labels (e.g. "Model_B1", "Model_B2"), each
    #                 containing {short_description, long_description}
    model_result_path: str = "Judge/input/app_name_only.json"

    # -----------------------------------------------------------------------
    # Output / runtime config
    # -----------------------------------------------------------------------
    OUTPUT_FILE     = "Judge/output/app_name_only_output.json"
    PROMPT_TEMPLATE = "Judge/judge_prompt.txt"
    NUM_WORKERS     = 15

    # -----------------------------------------------------------------------
    # Validate required files before starting
    # -----------------------------------------------------------------------
    if not os.path.exists(ground_truth_path):
        logger.error(f"Ground truth file not found: '{ground_truth_path}'")
        sys.exit(1)

    if not os.path.exists(model_result_path):
        logger.error(f"Model result file not found: '{model_result_path}'")
        sys.exit(1)

    if not os.path.exists(PROMPT_TEMPLATE):
        logger.error(f"Prompt template not found: '{PROMPT_TEMPLATE}'")
        sys.exit(1)

    try:
        judge = VariableJudge(
            prompt_template_path=PROMPT_TEMPLATE,
            num_workers=NUM_WORKERS,
        )
        judge.process(
            ground_truth_path = ground_truth_path,
            model_result_path = model_result_path,
            output_file       = OUTPUT_FILE,
        )

        logger.info("Judging completed successfully")
        print(f"\nResults saved to : {OUTPUT_FILE}")
        print(f"Log saved to     : judge_log.txt")

    except Exception as e:
        logger.error(f"Fatal error: {e}")
        print(f"\nFatal error occurred: {e}")
        sys.exit(1)


if __name__ == "__main__":
    main()


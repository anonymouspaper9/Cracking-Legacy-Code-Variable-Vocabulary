#!/usr/bin/env python3
"""
COBOL Variable Description Judge
---------------------------------
Reads two separate input files, joins them, then asks an LLM agent to judge
each model's short/long description quality against the ground truth and the
live repository.

Ground Truth file  (ground_truth_path):
    JSON array.  Fields used per record:
      - variable_name
      - R1_id
      - application                      -> app_name for repo path resolution
      - annotated_short_description_R2   -> short_description_ground_truth
      - annotated_long_description_R2    -> long_description_ground_truth

Model Result file  (model_result_path):
    JSON array.  Fields used per record:
      - variable_name
      - R1_id
      - app_name
      - bob_output (dict) with named model sub-keys, each containing:
            {short_description, long_description}
        Example:
            "bob_output": {
              "variable_name": "...",
              "Model_B1": {"short_description": "...", "long_description": "..."},
              "Model_B2": {"short_description": "...", "long_description": "..."}
            }
        Model labels are discovered dynamically at runtime — all keys except
        "variable_name" are treated as model identifiers.

Join:
    Inner join on (variable_name, R1_id).  Enforces strict 1-1 mapping —
    duplicate join keys within either file are treated as an error.

Unlike a plain LLM evaluator, the agent receives the local repository path so
it can fetch additional evidence beyond the manual ground truth. The GT is the
primary scoring direction; the repository lets the agent calibrate scores when
the GT is silent on a detail or when a model adds/contradicts code evidence.

Runs parallel processing with configurable workers, retry logic, and full
cost tracking — mirroring the structure of Annotation/annotator.py.
"""

import json
import os
import sys
from pathlib import Path
from concurrent.futures import ThreadPoolExecutor, as_completed
from typing import Dict, List, Any, Optional, Tuple
import logging

# Add parent directory to path to import llm_service
sys.path.insert(0, str(Path(__file__).parent.parent))

from llm_service.factory import LLMProviderFactory
from llm_service import parse_llm_json_response, JSONExtractionError

# ---------------------------------------------------------------------------
# Logging
# ---------------------------------------------------------------------------
logging.basicConfig(
    level=logging.INFO,
    format='%(asctime)s - %(levelname)s - %(message)s',
    handlers=[
        logging.FileHandler('judge_log.txt'),
        logging.StreamHandler()
    ]
)
logger = logging.getLogger(__name__)


# ---------------------------------------------------------------------------
# Application -> repository path mapping (same source-of-truth as annotator.py)
# ---------------------------------------------------------------------------

_BASE_PATH = os.environ.get("COBOL_APPS_BASE_PATH", "./data/applications")

APPLICATION_PATHS: Dict[str, str] = {
    "Z390DEV":               f"{_BASE_PATH}/X-COBOL/COBOL_Files/z390development_z390",
    "z390development_z390":  f"{_BASE_PATH}/X-COBOL/COBOL_Files/z390development_z390",
    "GENAPP":                f"{_BASE_PATH}/known_apps/COBOL_Files/GENAPP",
    "ETALAB":                f"{_BASE_PATH}/X-COBOL/COBOL_Files/etalab_taxe-fonciere",
    "etalab_taxe-fonciere":  f"{_BASE_PATH}/X-COBOL/COBOL_Files/etalab_taxe-fonciere",
    "CARDDEMO":              f"{_BASE_PATH}/known_apps/COBOL_Files/AWS_CARDDEMO",
    "AWS_CARDDEMO":          f"{_BASE_PATH}/known_apps/COBOL_Files/AWS_CARDDEMO",
    "DEBINIX":               f"{_BASE_PATH}/X-COBOL/COBOL_Files/debinix_pbs",
    "debinix_pbs":           f"{_BASE_PATH}/X-COBOL/COBOL_Files/debinix_pbs",
    "CRYPTOCOB":             f"{_BASE_PATH}/X-COBOL/COBOL_Files/DoHITB_CryptoCobol",
    "DoHITB_CryptoCobol":    f"{_BASE_PATH}/X-COBOL/COBOL_Files/DoHITB_CryptoCobol",
    "ZECS":                  f"{_BASE_PATH}/X-COBOL/COBOL_Files/walmartlabs_zECS",
    "walmartlabs_zECS":      f"{_BASE_PATH}/X-COBOL/COBOL_Files/walmartlabs_zECS",
}

# Join key type alias: (variable_name, application/app_name)
JoinKey = Tuple[str, str]


# ---------------------------------------------------------------------------
# Judge class
# ---------------------------------------------------------------------------

class VariableJudge:
    """
    Judges COBOL variable descriptions generated by multiple models.

    Reads ground truth (GT) and one or more model result (MR) files, performs
    an inner join on (variable_name, application/app_name), then judges each
    joined entry. The agent receives the local repository path for additional
    evidence.
    """

    MAX_RETRIES = 3      # Maximum retry attempts per variable
    TIMEOUT     = 300    # LLM call timeout in seconds (5 minutes)

    def __init__(self, prompt_template_path: str, num_workers: int = 5):
        """
        Initialize the judge.

        Args:
            prompt_template_path: Path to judge_prompt.txt
            num_workers: Number of parallel worker threads
        """
        self.num_workers = num_workers
        self.prompt_template_path = prompt_template_path
        self.prompt_template = self._load_prompt_template(prompt_template_path)
        self.llm_client = self._initialize_llm_client()

        self.results: List[Dict[str, Any]] = []
        self.errors:  List[Dict[str, Any]] = []
        self.total_cost           = 0.0
        self.failed_attempts_cost = 0.0
        self.skipped_model_outputs: List[Dict[str, Any]] = []

    # ------------------------------------------------------------------
    # Initialisation helpers
    # ------------------------------------------------------------------

    def _load_prompt_template(self, template_path: str) -> str:
        """Load the judge prompt template from disk."""
        try:
            with open(template_path, 'r', encoding='utf-8') as f:
                template = f.read()
            logger.info(f"Loaded prompt template from {template_path}")
            return template
        except Exception as e:
            logger.error(f"Failed to load prompt template: {e}")
            raise

    def _initialize_llm_client(self):
        """Initialise the LLM client using the Bob Shell provider."""
        try:
            api_key = os.getenv('BOBSHELL_API_KEY')
            if not api_key:
                raise ValueError("BOBSHELL_API_KEY environment variable not set")

            client = LLMProviderFactory.create(
                provider="bob_shell",
                api_key=api_key,
                timeout=self.TIMEOUT
            )
            logger.info(f"Initialised LLM client (Bob) with {self.TIMEOUT}s timeout")
            return client
        except Exception as e:
            logger.error(f"Failed to initialise LLM client: {e}")
            raise

    # ------------------------------------------------------------------
    # Repository path resolution
    # ------------------------------------------------------------------

    def _resolve_repository_path(self, entry: Dict[str, Any]) -> str:
        """
        Resolve the local filesystem path for the application's COBOL sources.

        Looks up app_name in APPLICATION_PATHS and verifies the path exists
        on disk.  Raises ValueError at any failure so the caller's retry
        loop surfaces the problem immediately.
        """
        variable_name = entry.get("variable_name", "UNKNOWN")
        app_name      = entry.get("app_name", "")

        if not app_name:
            raise ValueError(
                f"'{variable_name}': app_name is missing — cannot resolve repository path"
            )

        if app_name not in APPLICATION_PATHS:
            raise ValueError(
                f"'{variable_name}': app_name '{app_name}' is not registered "
                f"in APPLICATION_PATHS"
            )

        resolved = APPLICATION_PATHS[app_name]

        if not os.path.exists(resolved):
            raise ValueError(
                f"'{variable_name}': repository path does not exist on disk: {resolved}"
            )

        return resolved

    # ------------------------------------------------------------------
    # Data loading
    # ------------------------------------------------------------------

    def _load_json(self, path: str) -> List[Dict[str, Any]]:
        """Load a JSON file as a list of records (single dict is wrapped)."""
        logger.info(f"Loading: {path}")
        try:
            with open(path, 'r', encoding='utf-8') as f:
                data = json.load(f)
            if isinstance(data, dict):
                data = [data]
            logger.info(f"Loaded {len(data)} records from '{path}'")
            return data
        except Exception as e:
            logger.error(f"Failed to load '{path}': {e}")
            raise

    def _make_join_key(self, record: Dict[str, Any], source: str) -> Optional[JoinKey]:
        """
        Build the join key (variable_name, application/app_name) from a record.
        Returns None and logs an error if either field is missing.
        """
        variable_name = (record.get("variable_name") or "").strip()
        app_name      = (record.get("app_name") or record.get("application") or "").strip()

        if not variable_name:
            logger.error(f"[{source}] Record missing 'variable_name' — skipped")
            return None
        if not app_name:
            logger.error(
                f"[{source}] Record '{variable_name}' missing 'app_name/application' — skipped"
            )
            return None

        return (variable_name, app_name)

    def _index_records(
        self, records: List[Dict[str, Any]], source: str
    ) -> Dict[JoinKey, Dict[str, Any]]:
        """
        Index records by (variable_name, application/app_name).
        Raises ValueError if any duplicate join key is found — enforces 1-1.
        """
        index: Dict[JoinKey, Dict[str, Any]] = {}
        for rec in records:
            key = self._make_join_key(rec, source)
            if key is None:
                continue
            if key in index:
                raise ValueError(
                    f"[{source}] Duplicate join key (variable_name='{key[0]}', "
                    f"app_name='{key[1]}') — each (variable_name, app_name/application) pair must be unique"
                )
            index[key] = rec
        return index

    # ------------------------------------------------------------------
    # Join + entry assembly
    # ------------------------------------------------------------------

    def _extract_model_outputs(
        self,
        bob_output: Dict[str, Any],
        var_name: str,
        app_name: str,
        mr_path: str,
    ) -> List[Dict[str, str]]:
        """
        Extract all model outputs from a bob_output dict.

        The bob_output dict has the structure:
            {
              "variable_name": "...",          # skipped
              "Model_B1": {"short_description": "...", "long_description": "..."},
              "Model_B2": {"short_description": "...", "long_description": "..."},
              ...
            }
        Every key other than "variable_name" is treated as a model label.

        Returns a list of {model, short_description, long_description} dicts.
        Skips and tracks any model sub-entry that is malformed or empty.
        """
        outputs: List[Dict[str, str]] = []
        # Keys to skip — these are metadata, not model results
        SKIP_KEYS = {"variable_name"}

        for model_label, model_data in bob_output.items():
            if model_label in SKIP_KEYS:
                continue

            if not isinstance(model_data, dict):
                logger.warning(
                    f"('{var_name}', app_name='{app_name}') — bob_output['{model_label}'] "
                    f"is not a dict (got {type(model_data).__name__}) — skipping"
                )
                self.skipped_model_outputs.append({
                    "variable_name": var_name,
                    "app_name":      app_name,
                    "model":         model_label,
                    "source":        mr_path,
                    "reason":        f"bob_output['{model_label}'] is not a dict",
                })
                continue

            short = (model_data.get("short_description") or "").strip()
            long_ = (model_data.get("long_description")  or "").strip()

            missing_fields = []
            if not short:
                missing_fields.append("short_description")
            if not long_:
                missing_fields.append("long_description")

            if missing_fields:
                logger.warning(
                    f"('{var_name}', app_name='{app_name}') — bob_output['{model_label}'] "
                    f"empty field(s): {', '.join(missing_fields)} — included with empty strings"
                )

            outputs.append({
                "model":             model_label,
                "short_description": short,
                "long_description":  long_,
            })

        return outputs

    def _build_entries(
        self,
        ground_truth_path: str,
        model_result_path: str,
    ) -> List[Dict[str, Any]]:
        """
        Inner-join GT and MR records on (variable_name, application/app_name)
        and assemble judge-ready entry dicts.

        Args:
            ground_truth_path: Path to the GT JSON file.
            model_result_path: Path to the MR JSON file.

        Returns:
            List of merged entry dicts, each containing:
              - variable_name, app_name
              - app_name                        (from GT application field)
              - short_description_ground_truth  (from GT)
              - long_description_ground_truth   (from GT)
              - model_outputs  (list of {model, short_description, long_description}
                                extracted from bob_output sub-keys)
              - all original GT fields          (pass-through)
        """
        # ---- Load and index GT ------------------------------------------
        gt_records = self._load_json(ground_truth_path)
        gt_index   = self._index_records(gt_records, source=ground_truth_path)
        logger.info(f"GT: {len(gt_index)} unique (variable_name, application/app_name) keys")

        # ---- Load and index MR ------------------------------------------
        mr_records = self._load_json(model_result_path)
        mr_index   = self._index_records(mr_records, source=model_result_path)
        logger.info(f"MR: {len(mr_index)} unique (variable_name, application/app_name) keys")

        # ---- Inner join -------------------------------------------------
        # model_outputs_by_key[join_key] = [{model, short_description, long_description}, ...]
        model_outputs_by_key: Dict[JoinKey, List[Dict[str, str]]] = {}

        matched = missing = 0
        for key in gt_index:
            var_name, app_name = key

            if key not in mr_index:
                logger.warning(
                    f"Join miss — ('{var_name}', app_name='{app_name}') "
                    f"not found in MR file '{model_result_path}'"
                )
                model_outputs_by_key[key] = []
                missing += 1
                continue

            mr_rec  = mr_index[key]
            bob_out = mr_rec.get("formatted_output_masked")

            if not bob_out or not isinstance(bob_out, dict):
                logger.warning(
                    f"('{var_name}', app_name='{app_name}'): formatted_output_masked missing or "
                    f"not a dict in MR — skipping all models for this entry"
                )
                self.skipped_model_outputs.append({
                    "variable_name": var_name,
                    "app_name":      app_name,
                    "model":         "ALL",
                    "source":        model_result_path,
                    "reason":        "formatted_output_masked missing or not a dict",
                })
                model_outputs_by_key[key] = []
                missing += 1
                continue

            outputs = self._extract_model_outputs(
                bob_out,
                var_name,
                app_name,
                model_result_path,
            )
            model_outputs_by_key[key] = outputs
            matched += 1

        logger.info(
            f"Inner join: {matched} matched, {missing} unmatched/skipped "
            f"(join size = {matched} / GT size = {len(gt_index)})"
        )

        # Log MR keys with no GT counterpart (informational only)
        mr_only = set(mr_index.keys()) - set(gt_index.keys())
        if mr_only:
            logger.warning(
                f"{len(mr_only)} MR record(s) have no matching GT entry and were ignored"
            )

        # ---- Assemble final entry list -----------------------------------
        entries: List[Dict[str, Any]] = []

        for key, gt_rec in gt_index.items():
            var_name, app_name = key

            short_gt = (gt_rec.get("annotated_short_description_R2") or "").strip()
            long_gt  = (gt_rec.get("annotated_long_description_R2")  or "").strip()
            app_name = (
                gt_rec.get("application")
                or gt_rec.get("app_name")
                or ""
            ).strip()

            if not app_name:
                raise ValueError(
                    f"('{var_name}', app_name='{app_name}'): required GT field is empty: "
                    f"application/app_name"
                )

            outputs = model_outputs_by_key.get(key, [])
            if not outputs:
                logger.error(
                    f"('{var_name}', app_name='{app_name}'): no model outputs after join — "
                    f"entry will be judged with empty model_outputs"
                )

            entry = dict(gt_rec)                                   # GT pass-through
            entry["short_description_ground_truth"] = short_gt
            entry["long_description_ground_truth"]  = long_gt
            entry["app_name"]                       = app_name
            entry["model_outputs"]                  = outputs
            entries.append(entry)

        total_outputs = sum(len(e["model_outputs"]) for e in entries)
        logger.info(
            f"Built {len(entries)} judge entries — "
            f"{total_outputs} total model outputs "
            f"(avg {total_outputs / len(entries):.1f} models/entry)"
        )
        return entries

    # ------------------------------------------------------------------
    # Prompt preparation
    # ------------------------------------------------------------------

    def _prepare_prompt(self, entry: Dict[str, Any]) -> str:
        """
        Build the filled judge prompt for a single entry.
        Injects variable_name, GT descriptions, repository_path, and
        model_outputs into the {INPUT_JSON} placeholder.

        Raises ValueError if any required entry field is empty.
        """
        variable_name = entry.get("variable_name", "")
        short_gt      = entry.get("short_description_ground_truth", "")
        long_gt       = entry.get("long_description_ground_truth", "")
        model_outputs = entry.get("model_outputs", [])

        empty = [k for k, v in [
            ("variable_name",                  variable_name),
            ("short_description_ground_truth", short_gt),
            ("long_description_ground_truth",  long_gt),
        ] if not str(v).strip()]
        if empty:
            raise ValueError(
                f"Entry is missing required field(s): {', '.join(empty)}"
            )

        if not model_outputs:
            raise ValueError(
                f"Entry '{variable_name}' has no model_outputs to judge"
            )

        repository_path = self._resolve_repository_path(entry)

        input_payload = {
            "variable_name":                  variable_name,
            "short_description_ground_truth": short_gt,
            "long_description_ground_truth":  long_gt,
            "repository_path":                repository_path,
            "model_outputs":                  model_outputs,
        }
        return self.prompt_template.replace(
            "{INPUT_JSON}", json.dumps(input_payload, indent=2)
        )

    # ------------------------------------------------------------------
    # Per-entry judgment with retry
    # ------------------------------------------------------------------

    def _judge_entry(self, entry: Dict[str, Any], index: int) -> Dict[str, Any]:
        """
        Judge a single entry with retry logic.

        Returns a dict merging the original entry with a ``judge_output`` key,
        or an error dict if all retries are exhausted.
        """
        variable_name = entry.get("variable_name", "UNKNOWN")
        r1_id         = entry.get("R1_id", "?")

        for attempt in range(1, self.MAX_RETRIES + 1):
            attempt_cost = 0.0
            try:
                logger.info(
                    f"[{index}] Judging '{variable_name}' (R1_id={r1_id}) "
                    f"attempt {attempt}/{self.MAX_RETRIES}"
                )

                prompt   = self._prepare_prompt(entry)
                response, cost = self.llm_client.generate(prompt, return_cost=True)
                attempt_cost = float(cost) if cost is not None else 0.0
                logger.info(
                    f"[{index}] Attempt {attempt} cost for '{variable_name}': "
                    f"${attempt_cost:.4f}"
                )

                if not response or not response.strip():
                    raise ValueError("Empty response from LLM")

                # ---- Parse JSON response --------------------------------
                judgment: Dict[str, Any] = {}
                try:
                    try:
                        parsed = parse_llm_json_response(response, expected_type="object")
                    except JSONExtractionError:
                        parsed = parse_llm_json_response(response, expected_type="array")

                    if isinstance(parsed, list):
                        if not parsed:
                            raise ValueError("Empty list in parsed response")
                        judgment = dict(parsed[0]) if isinstance(parsed[0], dict) \
                                   else {"data": parsed}
                    elif isinstance(parsed, dict):
                        judgment = dict(parsed)
                    else:
                        judgment = {"data": parsed}

                except (JSONExtractionError, ValueError) as e:
                    logger.warning(
                        f"[{index}] Structured JSON extraction failed for "
                        f"'{variable_name}' (attempt {attempt}): {e}"
                    )
                    try:
                        raw = json.loads(response)
                        if isinstance(raw, list):
                            raw = raw[0] if raw and isinstance(raw[0], dict) else {}
                        judgment = dict(raw) if isinstance(raw, dict) else {"data": raw}
                    except json.JSONDecodeError:
                        self.failed_attempts_cost += attempt_cost
                        if attempt < self.MAX_RETRIES:
                            logger.warning(
                                f"[{index}] JSON decode failed for '{variable_name}', retrying..."
                            )
                            continue
                        logger.error(
                            f"[{index}] Failed after {self.MAX_RETRIES} attempts: '{variable_name}'"
                        )
                        return {
                            "variable_name": variable_name,
                            "R1_id":         r1_id,
                            "input_index":   index,
                            "error":         f"JSON parse failed after {self.MAX_RETRIES} attempts: {e}",
                            "raw_response":  response[:500],
                            "attempts":      attempt,
                            "original_data": entry,
                        }

                # ---- Validate required output fields --------------------
                evaluations = judgment.get("evaluations")
                if not evaluations or not isinstance(evaluations, list):
                    raise ValueError("Response missing required 'evaluations' list")

                # Success
                self.total_cost += attempt_cost

                judge_output = {
                    "variable_name":               judgment.get("variable_name", variable_name),
                    "repository_path":             judgment.get("repository_path", ""),
                    "manual_annotation":           judgment.get("manual_annotation", {}),
                    "repository_evidence_summary": judgment.get("repository_evidence_summary", ""),
                    "evaluations":                 evaluations,
                    "cost":                        attempt_cost,
                    "attempts":                    attempt,
                }

                final_result = {**entry, "judge_output": judge_output}
                logger.info(
                    f"[{index}] Successfully judged: '{variable_name}' "
                    f"(attempt {attempt}, cost: ${attempt_cost:.4f})"
                )
                return final_result

            except ValueError as e:
                self.failed_attempts_cost += attempt_cost
                logger.warning(
                    f"[{index}] Failed attempt {attempt} for '{variable_name}': "
                    f"{e} (cost: ${attempt_cost:.4f})"
                )
                if attempt < self.MAX_RETRIES:
                    logger.info(f"[{index}] Retrying '{variable_name}'...")
                    continue
                logger.error(
                    f"[{index}] Failed after {self.MAX_RETRIES} attempts: '{variable_name}'"
                )
                return {
                    "variable_name": variable_name,
                    "R1_id":         r1_id,
                    "input_index":   index,
                    "error":         f"{e} after {self.MAX_RETRIES} attempts",
                    "attempts":      attempt,
                    "original_data": entry,
                }

            except Exception as e:
                self.failed_attempts_cost += attempt_cost
                logger.error(
                    f"[{index}] Failed attempt {attempt} for '{variable_name}': "
                    f"{e} (cost: ${attempt_cost:.4f})"
                )
                if attempt < self.MAX_RETRIES:
                    logger.info(f"[{index}] Retrying '{variable_name}' after error...")
                    continue
                logger.error(
                    f"[{index}] Failed after {self.MAX_RETRIES} attempts: '{variable_name}'"
                )
                return {
                    "variable_name": variable_name,
                    "R1_id":         r1_id,
                    "input_index":   index,
                    "error":         f"Error judging '{variable_name}': {e} (failed after {self.MAX_RETRIES} attempts)",
                    "attempts":      attempt,
                    "original_data": entry,
                }

        # Should never reach here
        return {
            "variable_name": variable_name,
            "R1_id":         r1_id,
            "input_index":   index,
            "error":         "Unexpected error: exceeded retry loop",
            "original_data": entry,
        }

    # ------------------------------------------------------------------
    # Batch processing (public entry point)
    # ------------------------------------------------------------------

    def process(
        self,
        ground_truth_path: str,
        model_result_path: str,
        output_file: str,
    ) -> None:
        """
        Full pipeline: load → join → judge in parallel → save.

        Args:
            ground_truth_path: Path to the GT JSON file.
            model_result_path: Path to the MR JSON file.
            output_file:       Path for the judge results JSON.
        """
        entries = self._build_entries(ground_truth_path, model_result_path)

        logger.info(f"Starting parallel judging with {self.num_workers} workers")

        with ThreadPoolExecutor(max_workers=self.num_workers) as executor:
            future_to_index = {
                executor.submit(self._judge_entry, entry, idx): idx
                for idx, entry in enumerate(entries)
            }

            completed = 0
            total = len(entries)

            for future in as_completed(future_to_index):
                idx = future_to_index[future]
                try:
                    result = future.result()
                    if "error" in result:
                        self.errors.append(result)
                    else:
                        self.results.append(result)
                    completed += 1
                    logger.info(f"Progress: {completed}/{total} completed")
                except Exception as e:
                    logger.error(f"Task {idx} raised an exception: {e}")
                    self.errors.append({"input_index": idx, "error": str(e)})

        self._save_results(output_file)
        self._print_summary()

    # ------------------------------------------------------------------
    # Persistence and reporting
    # ------------------------------------------------------------------

    def _save_results(self, output_file: str) -> None:
        """Save results; write errors to a separate file if any."""
        try:
            with open(output_file, 'w', encoding='utf-8') as f:
                json.dump(self.results, f, indent=2, ensure_ascii=False)
            logger.info(f"Results saved to {output_file}")

            if self.errors:
                error_file = output_file.replace('.json', '_errors.json')
                with open(error_file, 'w', encoding='utf-8') as f:
                    json.dump(self.errors, f, indent=2, ensure_ascii=False)
                logger.info(f"Errors saved to {error_file}")
        except Exception as e:
            logger.error(f"Failed to save results: {e}")
            raise

    def _print_summary(self) -> None:
        """Print a processing summary to the log."""
        total      = len(self.results) + len(self.errors)
        success_rt = (len(self.results) / total * 100) if total > 0 else 0
        avg_cost   = self.total_cost / len(self.results) if self.results else 0

        summary = f"""
{'='*60}
JUDGE PROCESSING SUMMARY
{'='*60}
Total Entries:            {total}
Successfully Judged:      {len(self.results)}
Failed:                   {len(self.errors)}
Skipped Model Outputs:    {len(self.skipped_model_outputs)}
Success Rate:             {success_rt:.2f}%
Max Retries:              {self.MAX_RETRIES}
Timeout:                  {self.TIMEOUT}s
Total Cost (Successful):  ${self.total_cost:.4f}
Failed Attempts Cost:     ${self.failed_attempts_cost:.4f}
Grand Total Cost:         ${self.total_cost + self.failed_attempts_cost:.4f}
Average Cost/Entry:       ${avg_cost:.4f}
{'='*60}
"""
        logger.info(summary)


